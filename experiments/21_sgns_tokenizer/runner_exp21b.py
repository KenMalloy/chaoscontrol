#!/usr/bin/env python3
"""Exp 21b embedding-prior dissection.

This runner is deliberately SSM-only. Exp 21 already told us SGNS helps both
arms and that full-cov SGNS is basically indistinguishable from meanstd SGNS.
The next useful question is mechanism: which parts of the SGNS-derived init
are doing work?

Cells:
  - ``ssm_norm_only``: random directions, per-token SGNS/meanstd row norms.
  - ``ssm_norm_only_shuffled``: random directions, SGNS norm multiset shuffled.
  - ``ssm_freq_bucket_shuffle``: SGNS meanstd rows shuffled within frequency
    rank buckets.
  - ``ssm_class_bucket_shuffle``: SGNS meanstd rows shuffled within coarse
    token classes.
  - ``ssm_fullcov_shuffled``: full-cov control rows globally shuffled.
  - ``ssm_random_fullcov``: synthetic Gaussian cloud matched to fullcov mean/cov.

Artifacts are generated by ``scripts/prepare_sgns_inits.py``. Results land in
``results/exp21b_mechanisms`` as ``{condition}_s{seed}.json``.
"""
from __future__ import annotations

import argparse
import json
import re
import sys
from pathlib import Path
from typing import Any, Iterable

REPO = Path(__file__).resolve().parents[2]
EXPERIMENT = Path(__file__).resolve().parent
RESULTS = EXPERIMENT / "results" / "exp21b_mechanisms"
RUNNER = EXPERIMENT / "runner_exp21.py"

sys.path.insert(0, str(REPO / "experiments" / "18_throughput_levers"))
from _harness import run_parallel_ddp_matrix, validate_data_paths  # noqa: E402

sys.path.insert(0, str(EXPERIMENT))
from runner_4cell import SSM_LR, _ssm_cell  # noqa: E402


SEEDS = [1337, 42, 123, 7, 8]

MECHANISM_ARTIFACTS: dict[str, str] = {
    "ssm_norm_only": "artifacts/sgns_init_norm_only.pt",
    "ssm_norm_only_shuffled": "artifacts/sgns_init_norm_only_shuffled.pt",
    "ssm_freq_bucket_shuffle": "artifacts/sgns_init_freq_bucket_shuffle.pt",
    "ssm_class_bucket_shuffle": "artifacts/sgns_init_class_bucket_shuffle.pt",
    "ssm_fullcov_shuffled": "artifacts/sgns_init_fullcov_shuffled.pt",
    "ssm_random_fullcov": "artifacts/sgns_init_random_fullcov.pt",
}


def _parse_condition_names(raw: str | None) -> list[str] | None:
    if raw is None:
        return None
    names = [name.strip() for name in raw.split(",") if name.strip()]
    return names or None


def build_conditions(selected: Iterable[str] | None = None) -> dict[str, dict[str, Any]]:
    names = list(MECHANISM_ARTIFACTS) if selected is None else list(selected)
    unknown = [name for name in names if name not in MECHANISM_ARTIFACTS]
    if unknown:
        valid = ", ".join(MECHANISM_ARTIFACTS)
        raise ValueError(
            f"unknown Exp 21b condition(s): {unknown}; valid conditions: {valid}"
        )
    return {
        name: _ssm_cell(base_lr=SSM_LR, embed_init_path=MECHANISM_ARTIFACTS[name])
        for name in names
    }


def validate_embed_artifacts(conditions: dict[str, dict[str, Any]]) -> None:
    missing = []
    shape_errors = []
    for name, cfg in conditions.items():
        rel = cfg.get("embed_init_path")
        if rel is None:
            continue
        path = REPO / str(rel)
        if not path.is_file():
            missing.append(f"{name}: {rel}")
            continue
        import torch

        tensor = torch.load(path, map_location="cpu")
        expected = (int(cfg["vocab_size"]), int(cfg["model_dim"]))
        got = tuple(tensor.shape)
        if got != expected:
            shape_errors.append(f"{name}: {rel} shape mismatch got {got}, expected {expected}")
    if missing:
        raise FileNotFoundError(
            "missing Exp 21b init artifact(s); run scripts/prepare_sgns_inits.py "
            "with --token-counts/--data-dir-for-counts and --sp-model, or pass "
            "--conditions for the subset that exists:\n  " + "\n  ".join(missing)
        )
    if shape_errors:
        raise ValueError("invalid Exp 21b init artifact(s):\n  " + "\n  ".join(shape_errors))


def _collect(
    conditions: dict[str, dict[str, Any]],
    results_dir: Path,
) -> dict[str, dict[int, float]]:
    out: dict[str, dict[int, float]] = {}
    for name in conditions:
        out[name] = {}
        pattern = re.compile(rf"^{re.escape(name)}_s(\d+)\.json$")
        if not results_dir.exists():
            continue
        for file in results_dir.iterdir():
            m = pattern.match(file.name)
            if not m:
                continue
            data = json.loads(file.read_text())
            bpb = (
                float(data["eval"].get("bpb", float("nan")))
                if data.get("eval")
                else float("nan")
            )
            out[name][int(m.group(1))] = bpb
    return out


def summarize_results(
    conditions: dict[str, dict[str, Any]],
    *,
    results_dir: Path = RESULTS,
) -> dict[str, Any]:
    per_cell = _collect(conditions, results_dir)

    print("\nExp 21b mechanism results (per-seed bpb):")
    for name, by_seed in per_cell.items():
        seeds = sorted(by_seed.keys())
        if not seeds:
            print(f"  {name}: (no results)")
            continue
        bpbs = [by_seed[s] for s in seeds]
        mean = sum(bpbs) / len(bpbs) if bpbs else float("nan")
        print(f"  {name:<26} n={len(seeds)} mean_bpb={mean:.4f}  seeds={seeds}")

    return {
        "seeds": SEEDS,
        "artifact_paths": {
            name: cfg.get("embed_init_path") for name, cfg in conditions.items()
        },
        "per_cell": per_cell,
    }


def main() -> None:
    parser = argparse.ArgumentParser(
        description="Exp 21b SSM-only embedding-prior mechanism matrix"
    )
    parser.add_argument("--data-path", required=True)
    parser.add_argument("--sp-model-path", required=True)
    parser.add_argument("--budget", type=float, default=600.0)
    parser.add_argument("--num-slots", type=int, default=2)
    parser.add_argument(
        "--conditions",
        default=None,
        help="Optional comma-separated subset of Exp 21b conditions",
    )
    parser.add_argument("--summarize-only", action="store_true")
    args = parser.parse_args()

    conditions = build_conditions(_parse_condition_names(args.conditions))
    print(f"Exp 21b conditions: {', '.join(conditions)}")
    print(f"SSM LR: {SSM_LR}")

    if not args.summarize_only:
        validate_data_paths(args.data_path, args.sp_model_path)
        validate_embed_artifacts(conditions)
        run_parallel_ddp_matrix(
            conditions=conditions,
            seeds=SEEDS,
            ws_per_slot=2,
            num_slots=args.num_slots,
            data_path=args.data_path,
            sp_model_path=args.sp_model_path,
            budget=args.budget,
            results_dir=RESULTS,
            runner_script=RUNNER,
        )

    summary = summarize_results(conditions)
    RESULTS.mkdir(parents=True, exist_ok=True)
    (RESULTS / "exp21b_mechanisms_summary.json").write_text(
        json.dumps(summary, indent=2, default=str)
    )


if __name__ == "__main__":
    main()
